// n8n Code node — "AI fallback (stub summary)".
// Runs ONLY on the Anthropic node's error output (the HTTP call to Anthropic
// failed after all retries — e.g. a sustained 5xx, prod-observed).
// Without this, the whole call — INCLUDING the
// already-downloaded raw transcript — was dropped to the error branch.
//
// It synthesizes a minimal, schema-valid Anthropic-style response so the normal
// downstream path (Parse → Build commit payload → atomic Git Data commit) still
// runs and commits the RAW TRANSCRIPT + a stub summary in ONE commit. The .vtt
// is the irreplaceable artifact; the summary can be regenerated later.
//
// vtt_content and all call metadata are carried by $('Build Anthropic body'),
// which Parse reads — so we only need to hand Parse a valid body.content[0].text.

function fallbackSlug(topic) {
  const words = String(topic || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .split('-')
    .filter(Boolean)
    .slice(0, 6);
  // assertSchema requires 2–6 kebab words [a-z0-9-]; Cyrillic/empty topics
  // collapse to nothing → use a safe 2-word default.
  return words.length >= 2 ? words.join('-') : 'untitled-call';
}

const items = $input.all();
return items.map((it) => {
  const j = it.json;
  const errMsg = (j.error && (j.error.message || j.error.description))
              || (j.body && j.body.error && (j.body.error.message || j.body.error))
              || 'Anthropic call failed';

  const stub = {
    team_tag: 'mixed',                                  // → general/ folder (safe default)
    sub_tag: null,
    call_type: 'unspecified',
    tags: ['ai-summary-failed'],
    topic_slug: fallbackSlug(j.topic),
    confidence: 'low',
    slack_summary: '⚠️ AI summary unavailable — the Anthropic summarization call failed ('
      + String(errMsg).slice(0, 140)
      + '). The raw transcript was saved to the vault; the summary can be regenerated by re-running the call.',
    decisions: [],
    action_items: [],
    open_questions: [],
    key_insights: [],
    next_24_48h: [],
  };

  return {
    json: {
      ...j,
      __ai_fallback: true,
      // Shape Parse expects: response.body.content[0].text = JSON string.
      body: { content: [{ type: 'text', text: JSON.stringify(stub) }] },
    },
  };
});
